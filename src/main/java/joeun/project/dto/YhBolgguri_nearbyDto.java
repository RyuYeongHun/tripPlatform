package joeun.project.dto;

public class YhBolgguri_nearbyDto {
	private Double mapX;
	private Double mapY;
	private String title;
	private String firstimage;
	private Integer bolgguri_id;
	
	public YhBolgguri_nearbyDto() {}

	public YhBolgguri_nearbyDto(Double mapX, Double mapY, String title, String firstimage, Integer bolgguri_id) {
		super();
		this.mapX = mapX;
		this.mapY = mapY;
		this.title = title;
		this.firstimage = firstimage;
		this.bolgguri_id = bolgguri_id;
	}

	@Override
	public String toString() {
		return "YhBolgguri_nearbyDto [mapX=" + mapX + ", mapY=" + mapY + ", title=" + title + ", firstimage="
				+ firstimage + ", bolgguri_id=" + bolgguri_id + "]";
	}

	public Double getMapX() {
		return mapX;
	}

	public void setMapX(Double mapX) {
		this.mapX = mapX;
	}

	public Double getMapY() {
		return mapY;
	}

	public void setMapY(Double mapY) {
		this.mapY = mapY;
	}

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getFirstimage() {
		return firstimage;
	}

	public void setFirstimage(String firstimage) {
		this.firstimage = firstimage;
	}

	public Integer getBolgguri_id() {
		return bolgguri_id;
	}

	public void setBolgguri_id(Integer bolgguri_id) {
		this.bolgguri_id = bolgguri_id;
	}

	

}
