package joeun.project.dto;

public class API_contentImageDto {
	private String contentid;
	private String imgname;
	private String originimgurl;
	private String serialnum;
	private String cpyrhtDivCd;
	private String smallimageurl;

	public API_contentImageDto() {
	}

	public API_contentImageDto(String contentid, String imgname, String originimgurl, String serialnum,
			String cpyrhtDivCd, String smallimageurl) {
		super();
		this.contentid = contentid;
		this.imgname = imgname;
		this.originimgurl = originimgurl;
		this.serialnum = serialnum;
		this.cpyrhtDivCd = cpyrhtDivCd;
		this.smallimageurl = smallimageurl;
	}

	public String getContentid() {
		return contentid;
	}

	public void setContentid(String contentid) {
		this.contentid = contentid;
	}

	public String getImgname() {
		return imgname;
	}

	public void setImgname(String imgname) {
		this.imgname = imgname;
	}

	public String getOriginimgurl() {
		return originimgurl;
	}

	public void setOriginimgurl(String originimgurl) {
		this.originimgurl = originimgurl;
	}

	public String getSerialnum() {
		return serialnum;
	}

	public void setSerialnum(String serialnum) {
		this.serialnum = serialnum;
	}

	public String getCpyrhtDivCd() {
		return cpyrhtDivCd;
	}

	public void setCpyrhtDivCd(String cpyrhtDivCd) {
		this.cpyrhtDivCd = cpyrhtDivCd;
	}

	public String getSmallimageurl() {
		return smallimageurl;
	}

	public void setSmallimageurl(String smallimageurl) {
		this.smallimageurl = smallimageurl;
	}

	@Override
	public String toString() {
		return "API_contentImageDto [contentid=" + contentid + ", imgname=" + imgname + ", originimgurl=" + originimgurl
				+ ", serialnum=" + serialnum + ", cpyrhtDivCd=" + cpyrhtDivCd + ", smallimageurl=" + smallimageurl + "]";
	}


}
