package joeun.project.dto;

public class ThemeChoiceDto {
	
	private Integer choicetheme_id;
	private Integer choice_id;
	private String code;
	
	public ThemeChoiceDto() {}

	public ThemeChoiceDto(Integer choicetheme_id, Integer choice_id, String code) {
		super();
		this.choicetheme_id = choicetheme_id;
		this.choice_id = choice_id;
		this.code = code;
	}

	public Integer getChoicetheme_id() {
		return choicetheme_id;
	}

	public void setChoicetheme_id(Integer choicetheme_id) {
		this.choicetheme_id = choicetheme_id;
	}

	public Integer getChoice_id() {
		return choice_id;
	}

	public void setChoice_id(Integer choice_id) {
		this.choice_id = choice_id;
	}

	public String getCode() {
		return code;
	}

	public void setCode(String code) {
		this.code = code;
	}

	@Override
	public String toString() {
		return "ThemeChoiceDto [choicetheme_id=" + choicetheme_id + ", choice_id=" + choice_id + ", code=" + code + "]";
	}

	


}
