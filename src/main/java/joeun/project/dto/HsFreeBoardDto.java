package joeun.project.dto;

import java.time.LocalDateTime;

public class HsFreeBoardDto {
    private Integer id;
    private String title;
    private String content;
    private String writer;
    private LocalDateTime created_At;
    private LocalDateTime updated_At;
    private Integer views;
    private Integer likes;
    private Character is_Deleted;

    public HsFreeBoardDto() {}

    public HsFreeBoardDto(Integer id, String title, String content, String writer, LocalDateTime created_At,
            LocalDateTime updated_At, Integer views, Integer likes, Character is_Deleted) {
        super();
        this.id = id;
        this.title = title;
        this.content = content;
        this.writer = writer;
        this.created_At = created_At;
        this.updated_At = updated_At;
        this.views = views;
        this.likes = likes;
        this.is_Deleted = is_Deleted;
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getContent() {
        return content;
    }

    public void setContent(String content) {
        this.content = content;
    }

    public String getWriter() {
        return writer;
    }

    public void setWriter(String writer) {
        this.writer = writer;
    }

    public LocalDateTime getCreated_At() {
        return created_At;
    }

    public void setCreated_At(LocalDateTime created_At) {
        this.created_At = created_At;
    }

    public LocalDateTime getUpdated_At() {
        return updated_At;
    }

    public void setUpdated_At(LocalDateTime updated_At) {
        this.updated_At = updated_At;
    }

    public Integer getViews() {
        return views;
    }

    public void setViews(Integer views) {
        this.views = views;
    }

    public Integer getLikes() {
        return likes;
    }

    public void setLikes(Integer likes) {
        this.likes = likes;
    }

    public Character getIs_Deleted() {
        return is_Deleted;
    }

    public void setIs_Deleted(Character is_Deleted) {
        this.is_Deleted = is_Deleted;
    }

    @Override
    public String toString() {
        return "FreeBoardDto [id=" + id + ", title=" + title + ", content=" + content + ", writer=" + writer
                + ", created_At=" + created_At + ", updated_At=" + updated_At + ", views=" + views + ", likes=" + likes
                + ", is_Deleted=" + is_Deleted + "]";
    }
}
