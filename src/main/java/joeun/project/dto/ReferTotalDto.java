package joeun.project.dto;

public class ReferTotalDto {
	
	private Integer refertotal_id;         
	private Integer day_id;   
	private Integer bolgguri_id; 		
	private Integer choice_id;

	
	public ReferTotalDto () {}


	public ReferTotalDto(Integer refertotal_id, Integer day_id, Integer bolgguri_id, Integer choice_id) {
		super();
		this.refertotal_id = refertotal_id;
		this.day_id = day_id;
		this.bolgguri_id = bolgguri_id;
		this.choice_id = choice_id;
	}


	public Integer getRefertotal_id() {
		return refertotal_id;
	}


	public void setRefertotal_id(Integer refertotal_id) {
		this.refertotal_id = refertotal_id;
	}


	public Integer getDay_id() {
		return day_id;
	}


	public void setDay_id(Integer day_id) {
		this.day_id = day_id;
	}


	public Integer getBolgguri_id() {
		return bolgguri_id;
	}


	public void setBolgguri_id(Integer bolgguri_id) {
		this.bolgguri_id = bolgguri_id;
	}


	public Integer getChoice_id() {
		return choice_id;
	}


	public void setChoice_id(Integer choice_id) {
		this.choice_id = choice_id;
	}


	@Override
	public String toString() {
		return "ReferTotalDto [refertotal_id=" + refertotal_id + ", day_id=" + day_id + ", bolgguri_id=" + bolgguri_id
				+ ", choice_id=" + choice_id + "]";
	}

	
	

	
	
}
