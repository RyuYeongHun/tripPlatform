package joeun.project.dto;


public class YhArea_CodeDto {
	private String Areacode;
	private String rnum;
	private String name;
	
	public YhArea_CodeDto() {}

	public YhArea_CodeDto(String areacode, String rnum, String name) {
		super();
		Areacode = areacode;
		this.rnum = rnum;
		this.name = name;
	}

	@Override
	public String toString() {
		return "Area_CodeDto [Areacode=" + Areacode + ", rnum=" + rnum + ", name=" + name + "]";
	}

	public String getAreacode() {
		return Areacode;
	}

	public void setAreacode(String areacode) {
		Areacode = areacode;
	}

	public String getRnum() {
		return rnum;
	}

	public void setRnum(String rnum) {
		this.rnum = rnum;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	
	
}
