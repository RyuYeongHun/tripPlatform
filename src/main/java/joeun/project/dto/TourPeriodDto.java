package joeun.project.dto;

public class TourPeriodDto {
	
	private Integer period_id;
	private Integer period_nights;
	private String period_name;

	public TourPeriodDto() {}

	public TourPeriodDto(Integer period_id, Integer period_nights, String period_name) {
		super();
		this.period_id = period_id;
		this.period_name = period_name;
		this.period_nights = period_nights;
	}

	public Integer getPeriod_id() {
		return period_id;
	}

	public void setPeriod_id(Integer period_id) {
		this.period_id = period_id;
	}

	public String getPeriod_name() {
		return period_name;
	}

	public void setPeriod_name(String period_name) {
		this.period_name = period_name;
	}

	
	public Integer getPeriod_nights() {
		return period_nights;
	}

	public void setPeriod_nights(Integer period_nights) {
		this.period_nights = period_nights;
	}

	@Override
	public String toString() {
		return "TourPeriodDto [period_id=" + period_id + ", period_nights=" + period_nights + ", period_name="
				+ period_name + "]";
	}

	

	
	

}
